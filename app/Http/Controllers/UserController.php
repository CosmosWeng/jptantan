    /**
     * Display the specified resource.
     *
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
        $user = User::find($id);
        if (! $user) {
            return  redirect('/');
        }

        $token = $user->tokens->first();

        return view('pure.users.profile', [
            'user'   => $user,
            'token'  => $token ? $token->token : null,
        ]);
    }

